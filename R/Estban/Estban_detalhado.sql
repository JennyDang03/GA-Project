		estban.MES_EVTO_CD as data_base,
		estban.CONTA_ID,
		estban.SDAG_VLR_SDO,
		unicad.CNPJ, 
		municipio.MUN_CD_IBGE, 
		municipio.MUN_CD_CADMU		
FROM	COSDWPRO_ACC.COSTB_SDO_ESTBAN estban
LEFT JOIN BCBDWPRO_ACC.GEOTB_MUN_MUNICIPIO municipio
ON estban.MUNI_ID = municipio.MUN_CD_UNICAD
INNER JOIN	UNIDWPRO_ACC.UNIVW_CAU_CAD_UNICAD unicad
ON estban.PEBC_ID = unicad.ID_PESSOA_BC AND
   estban.MES_EVTO_CD = unicad.DATA
WHERE 
	estban.MES_EVTO_CD = @selectedDate 
